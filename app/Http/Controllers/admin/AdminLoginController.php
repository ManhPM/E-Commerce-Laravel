    public function authenticate(Request $request)
    {
        $validator = Validator::make($request->all(), [
            'email' => 'required|email',
            'password' => 'required|string|min:6'
        ]);

        if ($validator->passes()) {
            if (Auth::guard('admin')->attempt(['email' => $request->email, 'password' => $request->password], $request->get('remember'))) {
                $admin = Auth::guard('admin')->user();
                if ($admin->role == 1) {
                    return redirect()->route('admin.dashboard');
                } else {
                    Auth::guard('admin')->logout();
                    session()->put('error', 'You are not allowed to access this page');
                    return redirect()->route('admin.login');
                }
            } else {
                session()->put('error', 'Email or password is incorrect');
                return redirect()->route('admin.login');
            }
        } else {
            return redirect()->route('admin.login')->with('error', 'Validation Error')->withErrors($validator)->withInput($request->only('email'));
        }
    }
